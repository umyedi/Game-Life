def random_grid(width:int, height:int):
    """Generate a grid with random values.

    Args:
        width (int): width of the grid (number of elements in a sublist)
        height (int): height of the grid (number of sublist)

    Returns:
        list(list): grid with random values
    """
    grid = []
    for j in range(height):
        grid.append([])
        for _ in range(width):
            grid[j].append(choices(population=[0, 1], weights=[0.8, 0.1], k=1)[0])
    return grid